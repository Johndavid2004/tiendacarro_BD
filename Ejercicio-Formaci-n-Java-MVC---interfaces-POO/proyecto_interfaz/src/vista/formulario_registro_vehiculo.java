    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // Obtener valores de los campos de texto
        String marca = jTextField1.getText();
        String modelo = jTextField2.getText();
        String placa = jTextField3.getText();
        String tipoVehiculo = (String) jComboBox1.getSelectedItem();

        // Validar que los campos no estén vacíos
        if (marca.isEmpty() || modelo.isEmpty() || placa.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Mostrar un mensaje de confirmación
        JOptionPane.showMessageDialog(this, "Vehículo Registrado:\n" +
            "Marca: " + marca + "\n" +
            "Modelo: " + modelo + "\n" +
            "Placa: " + placa + "\n" +
            "Tipo: " + tipoVehiculo, "Registro Exitoso", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton1ActionPerformed

