    public String getCombustibleSeleccionado() {
        if (radiogasolina.isSelected()) return "Gasolina";
        if (radiodiésel.isSelected()) return "Diésel";
        if (radioelectrico.isSelected()) return "Eléctrico";
        return null;
    }

    public String getColorSeleccionado() {
        if (radiorojo.isSelected()) return "Rojo";
        if (radionegro.isSelected()) return "Negro";
        if (radioazul.isSelected()) return "Azul";
        if (radioblanco.isSelected()) return "Blanco";
        return null;
    }

